	<div class="explainCodeSocial explain hide">
		<h1 class="homestead yellowph">Code Social</h1>
		<ul>
			Le “code social” est un modèle de description à destination des organisations et des projets 
			souhaitant s'inscrire dans des logiques de transparence, d'ouverture et de collaboration. 
			Il est assimilable à une carte d'identité mise en mouvement.
			Les objectifs du code social sont multiples :
			<li>Permettre une prise en compte de la diversité des enjeux liés à la conception et au développement d'un projet ou d'une organisation.</li>
			<li>Permettre une description fine et exhaustive de leurs enjeux et de leurs principes de fonctionnement.</li>
			<li>Permettre aux organisations et aux projets d'être pleinement transparents.</li>
			<li>Tisser la confiance en permettant à tous d'avoir accès au code source d'une organisation ou d'un projet.</li>
			<li>Permettre le développement de collaborations saines et durables en évitant les imprécisions, les postures seulement rhétoriques, les non-dits, les malentendus, les surprises …</li>
			<li>Outiller, encourager, favoriser les organisations et les projets qui accordent une importance particulière à la transparence et à la cohérence de leur démarche.</li>
		</ul>
		<ul>
			Le code social comporte la raison d'être d'une organisation ou d'un projet ainsi que 7 modèles permettant de les décrire :
			<li>Raison d'être : Vision, missions, valeurs</li>
			<li>Modèle social et humain,</li>
			<li>Modèle économique,</li>
			<li>Modèle technologique</li>
			<li>Modèle juridique et financier</li>
			<li>Modèle écologique</li>
			<li>Modèle artistique et culturel</li>
